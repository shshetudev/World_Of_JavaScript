// Example-1
var x = 5;
var y = 6;
var z = x + y;
console.log('z:',z);

// String variable
var pi = 3.14;
var person = "John Doe";
var answer = 'Yes I am!';
console.log("Answer: ", answer);

// One statement, Many variables
var person = "John Doe", carName = "Volvo", price = 200;
console.log("Person: ", person);
console.log("Car Name: ", carName);
console.log("Person: ", price);

// Value = undefined
var carName;
console.log('Cart name: ', carName);

// Dollar Sign `$`
var $ = 2;
var $myMoney = 5;
console.log('My money: ', $myMoney);

// Underscore sign `_`
var _x = 2;
var _100 = 5;
console.log('_x:',_x)
console.log('_100:',_100)